# 2. EVENT-DRIVEN vs EMOTION-DRIVEN (事件驱动 vs 情感驱动)
# =============================================================================
DRIVE_TYPE_COMPARISON = (
    "【事件驱动 vs 情感驱动 — 两种叙事动力】\n\n"
    "事件驱动型：\n"
    "- 故事由外部可见的事件串联：被打脸→升级→反打脸\n"
    "- 读者刺激来自事件结果：'他能不能打赢？'\n"
    "- 优点：节奏快、爽点明确、易上手\n"
    "- 缺点：开篇用S级事件后，后续A级/B级事件会让读者疲劳（阈值升高）\n"
    "- 适用：升级文、系统文、玄幻、都市异能\n\n"
    "情感驱动型（人物驱动型）：\n"
    "- 故事由人物内在动机、情感变化、人际关系推动\n"
    "- 事件悬念是一次性的，人物悬念是持续性的\n"
    "- 读者更容易代入、情感拉扯更绵长\n"
    "- 适用：追妻火葬场、虐文、年代亲情、悬疑\n\n"
    "本质：两者共同本质是'情绪的波动'。事件驱动通过外部冲突制造波动，情感驱动通过内部冲突和人际张力制造波动。\n"
    "不同题材可偏重不同驱动方式，也可混合使用——例如玄幻主线用事件驱动，感情线用情感驱动。"
)

