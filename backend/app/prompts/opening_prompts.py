# =============================================================================
# 3. THREE BASE POINTS (三大基点)
# =============================================================================
THREE_BASE_POINTS = (
    "【三大基点 — 前3章必须完成的根基】\n\n"
    "| 基点 | 内容 | 作用 | 完成时机 |\n"
    "|------|------|------|----------|\n"
    "| 人设基点 | 展示主角核心性格和处境 | 建立代入感和共情 | 前3章 |\n"
    "| 切入点基点 | 主角遭遇的第一个冲突/机遇 | 制造紧迫感和阅读动力 | 最好第1章 |\n"
    "| 金手指基点 | 展示主角独特优势 | 制造期待感和差异化 | 前3章 |\n\n"
    "顺序规则：困境型先人设，优势型先金手指。切入点必须跟主线强相关。\n\n"
    "模板一 — 困境切入型：\n"
    "1. 主角处于不利处境（人设基点=共情入口）\n"
    "2. 遭遇压迫/危机（切入点=紧迫感）\n"
    "3. 金手指激活/展示（金手指=反转期待）\n"
    "4. 初步解决问题但引出更大问题（钩子）\n\n"
    "模板二 — 优势切入型：\n"
    "1. 主角已有独特优势但被低估/隐藏（人设+金手指合一）\n"
    "2. 优势在某个场景中意外暴露（切入点）\n"
    "3. 周围人的认知被刷新（震惊反应）\n"
    "4. 暴露引来更大的关注/麻烦（钩子）"
)

# =============================================================================
# 4. COMMON OPENING PROBLEMS (开头常见问题)
# =============================================================================
OPENING_COMMON_PROBLEMS = (
    "【开头常见问题速查】\n\n"
    "| 问题 | 症状 | 修正操作 |\n"
    "|------|------|----------|\n"
    "| 信息传达不清 | 主角在哪/干什么/动机不明 | 开局交代背景和处境，明确目标再进事件 |\n"
    "| 人设逼格过低 | 见美女紧张/无意义内心戏 | 主角要有逼格，配角要有特色 |\n"
    "| 情绪缺失 | 战斗枯燥/看几段不想读 | 战斗中穿插情绪——恐惧、愤怒、决心 |\n"
    "| 节奏拖沓 | 出场过多无关人物/铺垫太长 | 删无关互动，尽快进主线 |\n"
    "| 金手指出场突兀 | 像写日记一样冒出金手指 | 用危机或困境触发金手指 |\n"
    "| 铺垫章掉追读 | 章节名暴露是铺垫 | 用有戏剧性的标题代替'铺垫''过渡' |\n\n"
    "改进方向：\n"
    "- 强情绪开头：从高潮事件或复仇类强情绪切入，重大事件不能写得像闹着玩\n"
    "- 情报与期待感：每个情报都要有期待价值，关键情报拉起'然后呢？'\n"
    "- 目标驱动：主角目标要交代清楚，打斗必须基于主角目标\n"
    "- 共鸣：通过角色处境产生共鸣，先让读者站在主角这边\n"
    "- 创意验证：每个创意点必须转化为具体的剧情冲突，而非仅停留在概念层面"
)

