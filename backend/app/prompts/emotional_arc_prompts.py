# 3. EXPECTATION MANAGEMENT 6 LAWS (期待感管理六法则)
# ---------------------------------------------------------------------------
EXPECTATION_6_LAWS = (
    "【期待感管理六法则】\n\n"
    "1. 期待最大化定律：爽点到来前一刻是全篇张力最高位置。将满足而未满足时，读者需求最大。\n"
    "   操作：不要把爽点一次性释放，拖到最后一刻。\n\n"
    "2. 信息顺序操纵：先植入什么信息，读者就先入为主往哪个方向想。控制信息顺序=控制读者预期。\n"
    "   操作：先给误导信息A，让读者自己走向错误结论，再用真相B击碎。\n\n"
    "3. 期待递增法：在基底期待上不断添加细节，注入更具体化的需求缺口。\n"
    "   操作：主角要打脸，先写反派有多嚣张（期待+1），再写观众多期待主角出手（期待+2），"
    "再写主角恰好有克制反派的底牌（期待+3）。\n\n"
    "4. 断期待禁止：下一个期待立起来之前，绝不能结束当前期待。\n"
    "   操作：闭环一个期待时，下一个开环必须已在运行。永远不让读者觉得「故事讲完了」。\n\n"
    "5. 下行情节安全原则：写情绪下行时，锅都是别人的，功都是主角的。\n"
    "   操作：主角受挫不是因为自身弱，是因为遭人暗算/信息不足/保护他人。\n"
    "   同时暗示主角有翻盘手段（读者看见了但反派不知道的东西）。\n\n"
    "6. 爽点递增对比：连续爽点逐级递增。\n"
    "   影响范围维度：个人→群体→社会\n"
    "   揭示深度维度：表象→本质→颠覆\n"
    "   身份落差维度：路人→大佬→全场震惊"
)

