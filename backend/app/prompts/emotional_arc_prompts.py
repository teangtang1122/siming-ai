# 5. TENSION / RELEASE CYCLE (调动→释放→爽)
# ---------------------------------------------------------------------------
TENSION_RELEASE_CYCLE = (
    "【调动→释放→爽 循环公式】\n\n"
    "平静 → 调动 → 释放 → 爽\n\n"
    "| 技法 | 调动 | 释放 | 结果 |\n"
    "|------|------|------|------|\n"
    "| 装逼打脸 | 反派嚣张 | 主角碾压 | 爽 |\n"
    "| 拉仇恨 | 负面情绪 | 恶人受罚 | 爽 |\n"
    "| 先抑后扬 | 打压 | 爆发 | 爽 |\n"
    "| 期待感 | 制造鸿沟 | 弥合鸿沟 | 爽 |\n\n"
    "实操要点：\n"
    "- 每个场景标注：读者当前情绪在哪个阶段？下一步是调动还是释放？\n"
    "- 只有调动没有释放 = 读者憋屈弃书\n"
    "- 只有释放没有调动 = 爽感平淡无力\n"
    "- 调动程度决定释放爽度：仇恨拉多满，打脸就多爽\n"
    "- 「麻烦消失」=失去读者的根本原因——每次解决麻烦必须同时埋下新麻烦"
)

