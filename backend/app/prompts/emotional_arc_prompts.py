# 2. MID-STORY PRESSURE TACTICS (中段加压四手段)
# ---------------------------------------------------------------------------
MID_STORY_PRESSURE = (
    "【中段加压四手段 — 防止中间疲软】\n\n"
    "1. 公开升级：把私下伤害搬到公开场合（家宴、公司会议、直播、典礼）。\n"
    "   私下羞辱只有当事人在场，公开羞辱让所有人看着——伤害放大一百倍。\n\n"
    "2. 双重背叛：已知一层背叛后再加一层（经济背叛+名誉背叛、立场反转+证据篡改）。\n"
    "   第一层让读者愤怒，第二层让读者绝望——绝望的时候再给希望，爽感翻倍。\n\n"
    "3. 代价加速：不行动的代价不断升高。\n"
    "   第一天不还手失去机会 → 第二天不还手当众羞辱 → 第三天不还手连累重要的人。\n\n"
    "4. 战略性沉默：主角暂不反击，沉默本身持续加压。\n"
    "   他人误读为懦弱 → 反派更放肆 → 旁观者开始质疑 → 读者憋屈但知道反转在即。\n"
    "   铁律：压抑的同时必须给读者信心暗示（主角的暗中布局/语气中的自信/旁观者中有人看出了端倪）。"
)

