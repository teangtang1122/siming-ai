# 4. EMOTION CORE FORMULA (情绪核心公式)
# ---------------------------------------------------------------------------
EMOTION_CORE_FORMULA = (
    "【情绪核心公式】\n\n"
    "故事 = 人类情绪 × 虚构世界\n\n"
    "写每个场景时分离三层情绪：\n"
    "1. 角色自己的情绪（角色感受到的）\n"
    "2. 文本传递的情绪（文本要让读者感受的）\n"
    "3. 读者实际感受的情绪（读者真正的体验）\n"
    "同一场景三层可以完全不同：角色在哭，文本在撩，读者在爽。\n\n"
    "「不该如此」是最根本的情绪驱动力：\n"
    "- 想要爽，必先不爽；想要公平，必先不公平；想要强大，必先不强大\n"
    "- 写诬告、不公、恃强凌弱，读者天生生出「不该如此」的念头\n"
    "- 「悬而未决」也是强驱动力——持续的未解决状态=持续的关注和期待\n\n"
    "驱动力公式：产生诉求→给予希望→努力解决→得偿所愿\n"
    "- 产生诉求：制造不爽——低地位/困境/威胁/不曾拥有\n"
    "- 给予希望：展示金手指=改变的希望=给读者看下去的动力\n"
    "- 努力解决：不能太快得偿所愿，困境分层递进\n"
    "- 得偿所愿：爽点释放，困境层级越高→爽感越强"
)

