# =============================================================================
# 2. 第一斧：羁绊铺设（前1/3）
# =============================================================================
AXE1_BOND_BUILDING = (
    "【第一斧：羁绊铺设 — 前1/3篇幅建立关系质感】\n\n"
    "目标：用具体物件/数字/细节建立关系质感，让读者相信这段关系是真实的。\n\n"
    "方法一 — 用具体数字建立时间感：\n"
    "'相恋八年'、'昏迷五年'、'七年光景'、'等了三千二百一十五天'\n"
    "数字比形容词有力一百倍。'他们很相爱' → 改为 '她每天凌晨四点起来熬粥，熬了八年。'\n\n"
    "方法二 — 用具体物件承载感情：\n"
    "- 金锁（姐姐的生日礼物，后来发现是金包铜）\n"
    "- 账本数字（还债记录，每一笔都是屈辱）\n"
    "- 木头小马（父王亲手磨的，磨得很光滑）\n"
    "物件=情感的容器。物件越具体，情感越可触摸。\n\n"
    "方法三 — 用重复动作建立习惯：\n"
    "'每天一碗粥'、'每两周来看一次'、'每年冬天冻得睡不着'\n"
    "习惯建立后打破习惯=制造情绪转折。\n\n"
    "核心：羁绊越具体，后面的撕裂越痛。绝不用'他们很相爱'这类抽象描述。"
)

# =============================================================================
# 3. 第二斧：情感撕裂（中后段）
# =============================================================================
AXE2_EMOTIONAL_TEARDOWN = (
    "【第二斧：情感撕裂 — 中后段制造反差与撕裂】\n\n"
    "目标：制造反差，让读者以为恨对了→发现恨错了（或反过来）。\n\n"
    "方法一 — 反差法：\n"
    "先展示温暖一面，再用残酷真相击碎。\n"
    "示例：妈妈表面软弱可怜处处为女儿着想 → 实际一直操控姐姐索取钱财 → 金锁是假的（金包铜）→ 反差撕裂\n\n"
    "方法二 — 错位法：\n"
    "角色A以为在保护角色B，实际上在伤害。\n"
    "示例：王氏表面温柔（送手炉、煎汤药）→ 实际下药逼疯先皇后 → 用诛心话逼死\n"
    "错位的核心=善意包装的恶意，或恶意包装的善意。\n\n"
    "方法三 — 延迟真相法：\n"
    "关键信息在读者最不期待的时候揭示。\n"
    "不要在角色吵架时揭示真相——在角色最平静、最没有防备的时候揭示。\n"
    "示例：女主死后，男主整理遗物时发现一本日记——这才知道她为自己做过什么。\n\n"
    "核心：撕裂的力度取决于铺垫的厚度。没有好的羁绊铺设，撕裂就是无根之木。"
)

# =============================================================================
# 4. 第三斧：余韵钝痛（结尾）
# =============================================================================
AXE3_LINGERING_PAIN = (
    "【第三斧：余韵钝痛 — 结尾用安静细节击穿读者防线】\n\n"
    "目标：不用大哭大闹，用安静细节制造'看完之后很久都忘不掉'的效果。\n\n"
    "方法一 — 用日常动作承载巨大情感：\n"
    "继续喂粥、把衣服叠好、不回头、把门轻轻关上。\n"
    "最痛的不是嚎啕大哭，是'他像往常一样把粥喝完，碗放在桌上，轻轻说了句我走了'。\n\n"
    "方法二 — 用物件细节制造余韵：\n"
    "坏掉的金锁、磨得光滑的木头小马、沾血的戒指、褪色的照片。\n"
    "物件在结尾再次出现=情感闭环。开头出现过的物件在结尾以不同方式出现=杀伤力最大的余韵。\n"
    "示例：'他拿着那匹被我磨得很光滑的木头小马……他握着正好。'——磨损的痕迹=时间的重量。\n\n"
    "方法三 — 用'不'制造留白：\n"
    "'不解释、不回头、不流泪'——不做什么比做了什么更有力量。\n"
    "留白让读者自己脑补，脑补的内容永远比写出来的更打动自己。\n\n"
    "核心：最好的余韵不是大哭，是安静的钝痛。结尾不要解释情感，展示余波。"
)

# =============================================================================
# 5. EMOTIONAL PULLING RHYTHM (情感拉扯节奏)
# =============================================================================
EMOTIONAL_PULLING_RHYTHM = (
    "【情感拉扯节奏设计 — 基本节奏曲线】\n\n"
    "完整曲线：温暖 → 残忍 → 善意 → 真相 → 原谅 → 来不及 → 释然 → 细节暴击\n\n"
    "不是所有故事都走完整曲线。按题材选择子集：\n"
    "| 题材 | 典型节奏 | 核心拉扯 |\n"
    "|------|----------|----------|\n"
    "| 世情/爽文 | 欺压→忍耐→爆发→打脸 | 情绪反弹快、打脸密度高 |\n"
    "| 情感/虐心 | 甜蜜→破碎→挽回→错过 | 慢铺垫、后劲大 |\n"
    "| 古言/复仇 | 隐忍→布局→揭穿→报应 | 设定简洁、暴力美学 |\n"
    "| 悬疑/推理 | 平静→不安→真相→震惊 | 信息差制造不安 |\n"
    "| 年代/亲情 | 误解→冲突→真相→和解 | 代际冲突、时代质感 |\n\n"
    "拉扯原则：\n"
    "1. 每3-5个小节有一次情绪转向（不能一路虐到底或一路爽到底）\n"
    "2. 每次转向都要有触发事件（不能无理由地改变情绪）\n"
    "3. 最后一次转向决定结尾余韵的基调\n"
    "4. 爽文允许快速反弹（受辱后2-3节内打脸），虐文需要更长铺垫"
)

# =============================================================================
# 6. GENRE-SPECIFIC EMOTIONAL STRATEGIES
# =============================================================================
EMOTIONAL_GENRE_STRATEGIES = (
    "【按题材的情感策略差异】\n\n"
    "世情/爽文：情绪反弹快(受辱后2-3节内打脸)，打脸密度每3-5节一次，反派嚣张度递增，结尾痛快解气\n\n"
    "情感/虐心：羁绊细节密度高(前1/3必须建立深厚羁绊)，反差先暖后冷先甜后苦，余韵安静结尾+物件细节，结尾意难平释然\n\n"
    "古言/复仇：设定简洁人物关系清晰，暴力美学打脸直接不拖泥带水，最后1/4揭示最大底牌，结尾大快人心因果报应\n\n"
    "悬疑/推理：信息差布局(读者知道角色不知道或反过来)，排除法逐步排除可能性，先揭示做了什么再揭示为什么做，结尾细思极恐原来如此\n\n"
    "年代/亲情：代际冲突不站队展示双方苦，时代细节用时代特有的物件/习俗建立质感，不急和解先让双方充分受伤，结尾温暖中带着遗憾\n\n"
    "霸总/甜宠：甜密度决定粘性(每章至少一个甜场景)，宠要'只有他能做到的方式'，虐不超过全文20%，阻力用于推进感情不是真正威胁"
)

# =============================================================================
# 7. EMOTIONAL FAILURE MODES (失败模式)
# =============================================================================
EMOTIONAL_FAILURE_MODES = (
    "【情感设计常见失败模式】\n\n"
    "| 失败模式 | 识别方法 | 修正方向 |\n"
    "|----------|----------|----------|\n"
    "| 太平 | 连续5+节没有情绪转折 | 插入意外事件或新信息 |\n"
    "| 太赶 | 重大转折只用1节铺垫 | 给转折至少3节铺垫 |\n"
    "| 假虐 | 读者不心疼，只是看着难受 | 检查羁绊铺设是否具体（抽象→具体物件）|\n"
    "| 割裂 | 前后像两篇不同故事 | 用伏笔/物件/主题贯穿 |\n"
    "| 烂尾 | 反转后拖2000字还在交代 | 反转后500字内收尾 |\n"
    "| 人设崩 | 关键时刻行为不符合人设 | 回顾人设，确保行为逻辑一致 |"
)

