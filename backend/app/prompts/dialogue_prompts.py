def build_dialogue_system_prompt() -> str:
    """Build the full dialogue design guidelines for inclusion in system prompts."""
    return "\n\n".join([
        DIALOGUE_CORE_RULES,
        DIALOGUE_POWER_DYNAMICS,
        SUBTEXT_TECHNIQUES,
        DIALOGUE_RHYTHM,
        DIALOGUE_EMOTION_ESCALATION,
        CHARACTER_VOICE_DIFFERENTIATION,
        CROWD_DIALOGUE_RULES,
        DIALOGUE_SCENE_TYPES,
        build_dialogue_quality_check_prompt(),
    ])


def build_dialogue_quality_check_prompt() -> str:
    """Build a dialogue-specific quality self-check prompt."""
    return "\n".join([
        "【对话质量自查清单】",
        "- 每句对话是否有明确目的（推进剧情/展示人设/制造张力）？",
        "- 能否遮住角色名后区分是谁在说话？",
        "- 是否存在大段信息必须用对话来展示（啰嗦）？",
        "- 对话是否为问答式的一问一答（机械）？",
        "- 权力博弈是否清晰（短句方=权力上位）？",
        "- 是否有潜台词（真正含义在表面之下）？",
        "- 节奏是否有快慢交替？",
        "- 结尾对话能否预示接下来的节奏变化？",
        "- 是否存在配角无脑夸主角、互相解释已知信息？",
    ])
