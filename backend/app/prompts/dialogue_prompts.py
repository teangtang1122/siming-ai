"""Dialogue design guide — power dynamics, subtext, rhythm, character differentiation."""

from __future__ import annotations

# ---------------------------------------------------------------------------
# 1. DIALOGUE CORE PRINCIPLES
# ---------------------------------------------------------------------------
DIALOGUE_CORE_RULES = (
    "【对话核心规则 — 每句对话必须承载以下至少一项，否则删除】\n"
    "1. 推进剧情：透露新信息、推动事件发展\n"
    "2. 增加期待感：暗示即将发生的事、制造悬念\n"
    "3. 展示人设：通过语言风格传递角色性格\n\n"
    "绝对禁止：\n"
    "  - 配角无脑夸主角\n"
    "  - 互相解释读者已知信息（「正如你所知……」）\n"
    "  - 大段说明文式对话\n"
    "  - 所有角色说话方式一样\n"
    "  - 信息倾倒——角色彼此说对方早已知道的信息\n"
    "  - 过度礼貌——真实对话更粗糙直接\n"
)

# ---------------------------------------------------------------------------
# 2. POWER DYNAMICS IN DIALOGUE (对话权力博弈)
# ---------------------------------------------------------------------------
DIALOGUE_POWER_DYNAMICS = (
    "【对话权力博弈 — 对话长度 = 权力地位】\n\n"
    "掌控者话短且冷静，被动者话多且情绪化。\n\n"
    "三种权力模式：\n\n"
    "1. 压制模式\n"
    "   结构：对方长篇大论（3-5行）-> 主角一字回应\n"
    "   示例：\n"
    "   「你以为你是什么东西？我告诉你，这个家轮不到你说话！"
    "你嫁进来的那天起就该明白自己的位置——」\n"
    "   「滚。」\n\n"
    "2. 反转模式\n"
    "   结构：对方嚣张（2-3行）-> 主角亮底牌（1行事实）-> 对方沉默\n"
    "   示例：\n"
    "   「你有什么资格管？这是我家的钱，我想怎么花怎么花。」\n"
    "   「你妈的存折，密码是我的生日。」\n\n"
    "3. 心死模式\n"
    "   结构：对话越回越短，从辩解到沉默到「随意」\n"
    "   示例：\n"
    "   「你听我解释，那天不是你想的那样——」\n"
    "   「嗯。」\n"
    "   「真的，我可以证明——」\n"
    "   「随意。」\n\n"
    "操作指令：\n"
    "  - 掌控者/主角亮底牌时：对话≤10字，不加动作描写\n"
    "  - 被压制方：对话≥20字，可加动作描写（攥拳/咬唇/站起来）\n"
    "  - 两人对话时：短句方=权力上位，长句方=权力下位\n"
)

# ---------------------------------------------------------------------------
# 3. SUBTEXT TECHNIQUES (潜台词技法)
# ---------------------------------------------------------------------------
SUBTEXT_TECHNIQUES = (
    "【潜台词技法 — 真正含义在表面之下】\n\n"
    "角色真实动机绝对不能浅显地写在台词里。\n"
    "每句对白同时设计：角色的动机（可能角色自己都没意识到）+ 角色的借口。\n\n"
    "四种潜台词技巧：\n\n"
    "1. 话题转移\n"
    "   「你爱我吗？」\n"
    "   「你看了天气预报吗？明天有雨。」\n\n"
    "2. 反问而非回答\n"
    "   「你杀了他吗？」\n"
    "   「你觉得像我这样的人会做那种事？」\n\n"
    "3. 谈论其他事物\n"
    "   「你想我吗？」\n"
    "   「我妈昨天打电话来了。」\n\n"
    "4. 沉默和动作\n"
    "   「你愿意原谅我吗？」\n"
    "   她继续看杂志，翻了一页。\n\n"
    "对话议程：\n"
    "  - 每个角色进入对话时有自己的议程——想从这场对话中得到什么\n"
    "  - 两个角色的议程碰撞 = 张力来源\n"
    "  - 双方议程一致 = 复述，失去意义\n\n"
    "语气三要素：关系 × 场合 × 目的 = 语气\n"
    "  - 私人场合（单对单）：深入、感情流露透彻\n"
    "  - 公众场合：需考虑体面，措辞收敛；需要冲击力时可打破\n"
    "  - 熟人/同门：深度介于两者之间\n"
)

# ---------------------------------------------------------------------------
# 4. DIALOGUE RHYTHM CONTROL (对话节奏)
# ---------------------------------------------------------------------------
DIALOGUE_RHYTHM = (
    "【对话节奏控制】\n\n"
    "快节奏对话 — 短句、无标签、打断、快速升级（争吵/追逐/紧急决策）：\n"
    "  做法：去掉所有对话标签、单句成段不超过15字、允许不完整句子、用动作替代呼吸空间\n"
    "  示例：\n"
    "  「左边！」\n"
    "  「来不及了——」\n"
    "  「跳！」\n"
    "  水面越来越近。\n"
    "  「抓紧！」\n\n"
    "慢节奏对话 — 长句、停顿、迂回、潜台词丰富（告白/试探/审讯/告别）：\n"
    "  做法：在对话间插入动作描写和沉默、让角色绕弯子欲言又止、用环境细节控制呼吸感\n"
    "  示例：\n"
    "  「这些年，」他端起茶杯，看着水面上的茶叶慢慢沉下去，\n"
    "  「你有没有想过，如果当初我们没走那条路——」\n"
    "  「没有。」她很快回答。太快了。\n\n"
    "沉默的戏剧力量：\n"
    "  - 拒绝回答 -> 暗示隐瞒或保护\n"
    "  - 反应延迟 -> 暗示震惊或权衡\n"
    "  - 对话中断 -> 暗示情感崩溃或决心\n"
    "  - 以动作代替回答 -> 比语言更有力\n\n"
    "呼吸感规则：\n"
    "  - 连续多轮对话后需要「换气」——插入环境描写或角色心理\n"
    "  - 适当停顿比连续输出更有张力\n"
    "  - 「你确定？」比长篇解释更有压迫感\n"
    "  - 对话段落间穿插动作描写、环境变化、心理活动调节节奏\n"
)

# ---------------------------------------------------------------------------
# 5. DIALOGUE EMOTION 5-LEVEL ESCALATION (情绪五级递进)
# ---------------------------------------------------------------------------
DIALOGUE_EMOTION_ESCALATION = (
    "【对话情绪五级递进 — 同一冲突场景从弱到强】\n\n"
    "1级 客观陈述：「你把我的东西扔了。」\n"
    "2级 陈述+建议：「你把我的东西扔了，以后能不能先跟我说一声。」\n"
    "3级 主观指责：「你凭什么动我的东西。」\n"
    "4级 指责+命令：「你算什么东西，也配碰我的东西？滚出去。」\n"
    "5级 指责+PUA：「我伺候你吃伺候你穿，你连个东西都放不好。"
    "你这辈子也就是这样了，离了我你什么都不是。」\n\n"
    "最强话术：打着为你好幌子，句句不离关心，但句句都是嫌弃、指责、厌恶。\n"
    "直接否定比含蓄暗示更伤人。命令式+否定式最能激发读者情绪。\n"
)

# ---------------------------------------------------------------------------
# 6. CHARACTER LANGUAGE DIFFERENTIATION (人物语言差异化)
# ---------------------------------------------------------------------------
CHARACTER_VOICE_DIFFERENTIATION = (
    "【人物语言差异化 — 遮住角色名后应能区分是谁在说话】\n\n"
    "七维差异化：\n"
    "1. 口癖和惯用语：给每个主要角色一个标志性用词\n"
    "2. 说话节奏：长篇大论 vs 短句连击\n"
    "3. 信息偏好：技术型带专业术语，江湖人带切口\n"
    "4. 立场固定：某角色永远从某个角度发言（悲观派/乐观派/务实派）\n"
    "5. 性格影响语气：智谋型话里有话；鲁莽型想到什么说什么；冷静型措辞精确\n"
    "6. 社会地位标记：上位者用短句/命令式；下位者用长句/解释式\n"
    "7. 情绪外显程度：有人情绪全写在话里，有人越激动越沉默\n\n"
    "常见问题修正：\n"
    "  - 问答式（通篇一问一答像审讯）-> 改为一方主动说，另一方给反应\n"
    "  - 冲突式太礼貌 -> 递进五级往上推\n"
    "  - 日常对话像水 -> 确保每句承载核心功能\n"
    "  - 多人对话混乱 -> 写前规划每个角色功能（信息提供者/情绪放大器/冲突制造者）\n"
)

# ---------------------------------------------------------------------------
# 7. CROWD / BARRAGE DIALOGUE (群众弹幕对话)
# ---------------------------------------------------------------------------
CROWD_DIALOGUE_RULES = (
    "【群众弹幕/围观者对话设计】\n\n"
    "三大核心作用：剧情推进（透露主角不知道的信息）、增加期待感（悬念、猜测）、"
    "情绪渲染（群众震惊/激动传染读者）\n\n"
    "递进结构（设计过的弹幕）：\n"
    "  普通人震惊 -> 专业人士分析 -> 特殊身份者反应 -> 情感升华\n\n"
    "操作要点：\n"
    "  - 不同人格化语气，不能每条都一个味\n"
    "  - 短小精悍，每条不超过一句话核心信息\n"
    "  - 善用递进：从最初震惊到逐渐认识全貌\n"
    "  - 可出现「反转」角色：看似路人一句话改变所有人认知\n"
    "  - 不用每章都写，关键爽点/燃点/泪点前后集中使用\n\n"
    "震惊三层结构（从弱到强）：\n"
    "  1. 点震惊：一个人震惊了一下（最弱）\n"
    "  2. 网震惊：周围人都有反应，不同角色不同反应方式\n"
    "  3. 深度震惊：多层震惊叠加——成就1震惊->成就2震惊->成就3引爆\n"
)

