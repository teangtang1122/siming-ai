# 7. STRUCTURE OBJECT SYSTEM (结构物件三现)
# ---------------------------------------------------------------------------
STRUCTURE_OBJECT_RULE = (
    "【结构物件三现规则】\n\n"
    "每个叙事单元设计1-2个结构物件，每个必须出现3次，每次意义不同：\n"
    "  第1现（前1/4）：建立初始含义（金锁=姐姐送的生日礼物，温暖）\n"
    "  第2现（中段转折）：含义被颠覆（金锁=金包铜的假货，虚伪）\n"
    "  第3现（结尾）：情感暴击（金锁被丢进垃圾桶，彻底决裂）\n\n"
    "物件类型：信物型（承载关系象征）/ 工具型（日常功能性物件）/ "
    "痕迹型（残留身体/环境痕迹）/ 数字型（承载关系的具体金额/年限）\n"
    "物件设计模板：物件名称 + 第1现（节/场景/含义）+ "
    "第2现（节/场景/含义）+ 第3现（节/场景/含义）\n"
)

