# 5. KEY MOMENT SLOWDOWN (关键时刻放慢 — 子弹时间)
# ---------------------------------------------------------------------------
KEY_MOMENT_SLOWDOWN = (
    "【关键时刻放慢 — 子弹时间技法】\n\n"
    "重要时刻比实际时间慢得多：把一秒钟的事写成一段，"
    "一个动作分解成五个微动作，一个念头展开成三层思维。\n\n"
    "两种手法：\n"
    "  1. 动作分解：笼统动作拆成「准备—执行—意外—调整—完成」微步骤\n"
    "  2. 思维展开：动作间隙插入判断、回忆、预判和情感波动\n\n"
    "应用场景：战斗关键时刻、告白瞬间、发现真相、生死抉择。\n"
    "禁忌：日常琐事放慢 = 注水；紧张时刻一笔带过 = 泄气。\n"
)

