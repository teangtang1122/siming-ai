# 2. 3D SCENE WEAVING (三维度织入)
# ---------------------------------------------------------------------------
SCENE_WEAVING_RULE = (
    "【场景织入三维度规则 — 每个子事件必须包含三个维度】\n\n"
    "三个维度承载不同信息，织入同一段连续正文，禁止分段各写各的：\n"
    "  1. 发生（1-2句）：事情出现了，包含一个具体细节\n"
    "  2. 感知（至少1个感官细节）：主角注意到什么，聚焦一个物件或身体部位\n"
    "  3. 反应（具体身体动作）：身体如何回应，可含一句极短的心理定格\n\n"
    "绝对禁止：把三个维度分成三段依次写完（这是AI最常见的写作痕迹）：\n"
    "  ❌ 林父低着头，左手把文书压住，右手拿笔，往纸上落——\n"
    "     （空行）手在抖。\n"
    "     （空行）不是小抖，是从肘到腕的那种……\n"
    "  ✅ 林父左手压着文书，右手拿笔往纸上落——笔尖一触纸面就偏了，"
    "从肘到腕止不住地抖，那一横斜着拖出去。\n\n"
    "自检方法（三指遮盖法）：分别遮住发生/感知/反应信息，"
    "看每个维度是否都有内容。缺失则织入对应细节，而非在后面另起一段补写。\n\n"
    "镜头断段（防止一段到底）：\n"
    "  按镜头/信息变化断段，不按维度变化：\n"
    "  - 新动作开始 -> 另起一段\n"
    "  - 新物件/线索出现 -> 另起一段\n"
    "  - 角色视线转移 -> 另起一段\n"
    "  - 直接引语出现 -> 对话独立成段\n"
    "  - 心理定格/判断落下 -> 可以单句成段，制造停顿\n\n"
    "硬规则：\n"
    "  - 一段只承载一个镜头拍点/一个动作单元\n"
    "  - 常规叙事优先一段一句；细密描写最多2句\n"
    "  - 单段超过60字，优先按动作或信息变化拆开\n"
    "  - 单句超过45字，除非刻意放慢，否则拆成两句\n"
    "  - 一段里不要连续放3个句号；出现第3个句号前必须换行\n"
    "  - 连续4个以上≤12字短段时，合并同一镜头内的相邻句\n"
)

