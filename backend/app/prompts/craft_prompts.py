# 4. OPENING EVENT DENSITY (开头事件密度)
# ---------------------------------------------------------------------------
OPENING_DENSITY_RULE = (
    "【开头事件密度规则】\n\n"
    "前100字必须包含≥3个事件。不做背景铺垫，直接上事件链。\n\n"
    "操作指令：\n"
    "  - 删除所有开头背景介绍（「在古代XX年间」「她是一个XX的女孩」）\n"
    "  - 第1句就是事件，不是描述\n"
    "  - 写完开头后列出前100字的事件清单，<3个则重写\n\n"
    "示例：\n"
    "  ❌ 低密度（1件事/100字）：\n"
    "    「沈栀是沈家的嫡女，自幼聪慧，深得父亲喜爱。这一天，她收到了一道圣旨。」\n"
    "  ✅ 高密度（4件事/50字）：\n"
    "    「萧衍回来了。皇后死了。儿子被他皇弟打得痴傻。他提着刀进了宫。」\n"
)

