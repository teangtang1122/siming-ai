# 3. ONE-MOVE-ONE-STILL RHYTHM (一动一静节奏)
# ---------------------------------------------------------------------------
MOVE_STILL_RHYTHM = (
    "【一动一静节奏规则】\n\n"
    "每个小节内部交替动作和安静。动后必静，静后可动。\n\n"
    "动与静的分类：\n"
    "  动：物理冲击、暴力、冲突爆发——砍人、摔东西、打耳光、吐血、掀桌子\n"
    "  静：日常细节、微小动作、安静观察——擦灰、喂粥、叠衣服、吹浮沫、理衣领\n\n"
    "操作指令：\n"
    "  - 每个小节必须包含≥1个「动」+≥1个「静」\n"
    "  - 不允许连续2节全「动」（暴力疲劳）\n"
    "  - 不允许连续2节全「静」（节奏拖沓）\n"
    "  - 情绪最高点（打脸/反转/揭露）用「动」\n"
    "  - 情绪最低点（心死/余韵/释然）用「静」\n"
    "  - 子事件之间用身体动作连接（~20字），不用叙述过渡\n"
    "    ❌「然后我翻到下一页。」\n"
    "    ✅「我把账本搁在膝盖上，手心出一层薄汗。」\n"
)

