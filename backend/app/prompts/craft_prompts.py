# 6. CONTENT EXPANSION SIX METHODS (内容扩充六法)
# ---------------------------------------------------------------------------
EXPANSION_METHOD_1_SCENE = (
    "【扩充法一：场景肌理充实】\n"
    "场景不只是一个地点。充实三个维度：\n"
    "  感官层：视觉+听觉+触觉+嗅觉，至少覆盖两到三种\n"
    "  空间层：角色在空间中的位置、移动、与物体的距离关系\n"
    "  情绪层：环境如何映射或反衬角色当前的心理状态\n"
    "每一处细节同时服务于氛围营造和信息铺设。"
)

EXPANSION_METHOD_2_INNER = (
    "【扩充法二：内心世界展开 — 四种手法】\n"
    "  1. 思维链展开：一个念头引出下一个（A->B->C->与D矛盾）\n"
    "  2. 感官触发记忆：一个气味/旋律/触感自然地引出回忆\n"
    "  3. 内心对话：角色在脑子里自我质疑、自我安慰、自我欺骗\n"
    "  4. 身体反应映射心理：不写情绪，写身体——手抖、胃翻搅、后背发凉\n"
)

EXPANSION_METHOD_3_DIALOGUE = (
    "【扩充法三：对话层次丰富 — 四个层次】\n"
    "  1. 动作和表情中断：说话时翻文件、看手机、整理衣领——透露真实状态\n"
    "  2. 潜台词层：说出口的和真正想说的不是一回事\n"
    "  3. 话题绕弯再回来：先聊天气、食物，突然拐回核心话题\n"
    "  4. 对话中的权力博弈：谁先开口、谁沉默、谁转移话题——每个行为都是权力微调\n"
)

EXPANSION_METHOD_4_SUBPLOT = (
    "【扩充法四：次要情节穿插 — 四种方式】\n"
    "  1. 配角小故事片段：一个细节让配角也有自己的生活\n"
    "  2. 暗线一笔推进：一两句提到之前埋下的伏笔，保持活跃度\n"
    "  3. 伏笔自然埋设：不经意提到某人/某事/某物，后面揭示重要性\n"
    "  4. 关系微妙变化信号：每章角色关系都应有微小变化（一个眼神/动作差异）\n"
    "注意：次要情节不是打断主线，而是从主线缝隙中穿过去的暗线。"
)

EXPANSION_METHOD_5_SENSORY = (
    "【扩充法五：感官维度叠加】\n"
    "很多场景读起来「平」是因为只用了视觉。叠加2-3个感官维度：\n"
    "  恐怖场景 -> 加重嗅觉和触觉\n"
    "  浪漫场景 -> 加听觉和嗅觉\n"
    "  动作场景 -> 加触觉和听觉\n"
    "感官维度检查清单：视觉（颜色/光影）、听觉（环境音/沉默/节奏）、"
    "触觉（温度/质感/疼痛）、嗅觉（香气/腐败/金属味）、味觉（食物/血腥/苦涩）\n"
    "每个场景至少用2种以上感官。"
)

EXPANSION_METHOD_6_DIAGNOSIS = (
    "【扩充法六：分题材扩充策略】\n"
    "  悬疑/推理：核心是让信息以可控速度释放——线索分层呈现、嫌疑转移、调查过程细节\n"
    "  言情/都市：核心是把情感变化拆成微事件——情感微瞬间、关系张力、日常浪漫\n"
    "  奇幻/玄幻：核心是通过角色体验展示世界观——力量体系具身体验、文化仪式沉浸描写\n"
    "  历史/古风：核心是通过日常生活感受时代质感——物质细节、社会规则压迫、政治微观化\n\n"
    "防注水原则：\n"
    "  - 删除后故事变差？否 = 注水\n"
    "  - 禁止无意义风景描写、凑字数回忆、重复已知信息、过渡冗长、对话注水\n"
    "  - 每处扩充必须同时服务于故事（推进人物/氛围/关系中的至少一项）\n"
)

